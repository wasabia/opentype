library Math;

import 'dart:math' as math;

/*
* sss
*/
num sign(num v) {
  return v.sign;
}

int ceil(num v) {
  return v.ceil();
}

int floor(num v) {
  return v.floor();
}

num abs(num v) {
  return v.abs();
}

int round(num v) {
  return v.round();
}

num sqrt(num v) {
  return math.sqrt(v);
}

num pow(num x, num exponent) {
  return math.pow(x, exponent);
}

num log(num x) {
  return math.log(x);
}